# Syntax: The syntax for a lambda function is lambda arguments: expression.
# Usage: Lambda functions are typically used for short, throwaway functions,
#  often in places where a full function definition would be overkill.

# A simple lambda function that adds two numbers
add = lambda x, y: x + y

# Using the lambda function
result = add(3, 5)
print(result)  # Output: 8
 